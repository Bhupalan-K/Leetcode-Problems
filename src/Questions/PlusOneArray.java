package Questions;

public class PlusOneArray {

	public static void main(String[] args) {
		
          int d[] = {1,2,3,4,5};
          
		
		for(int i=0; i<d.length; i++) {
			int size = d.length;
			int a = d[size-1];
			int b = d[size-2];
			System.out.println(a);
			System.out.println(b);
		}
		
		
	}
	

}
